void main(){
  /// Fixd velu jonno jeta kokhono change hobe na tar jonno variable decler korar age "const" bebohar korte pari
  /// constent shate shate value boshaite hobe
  const int numberOfSchool = 5;
  print(numberOfSchool);


  /// finalo same kaj kore bt value pore boshano jai // akbari valu diya jabe
  final String name;
  name = 'sgor';
  print(name);

  /// data type shob khane var use kora jai.....
  var nam = 'abir';
  print(nam);

  /// Dynamic hocche jokhon khusi je kono typer data rakha jabe
  dynamic age = 34;
  age = 36;
  age = 'sagor';
  age = true;
  print(age);
}